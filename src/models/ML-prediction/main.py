from fastapi import FastAPI
import pandas as pd
import threading
from listener import get_latest_prediction

from model import load_model, predict
from listener import listen_for_updates, latest_prediction, prediction_lock

app = FastAPI(
    title="OPA Trading Signal API",
    description="""
This API provides real-time trading signals generated by a Random Forest machine learning model.

### How it works
- A trained Random Forest model is loaded at startup
- A background listener watches a PostgreSQL database for new 15-minute candle features
- When new data arrives, predictions are generated automatically
- The API exposes the latest prediction

### Prediction classes
- **1**  → Buy signal
- **0**  → Hold / no trade
- **-1** → Sell signal

### Key concept
This API is part of a streaming pipeline. It does NOT compute predictions per request,
but instead serves the most recent prediction generated in the background.
""",
    version="2.0"
)

@app.on_event("startup")
def startup():
    """
    FastAPI startup event handler.

    This function runs exactly once when the API server starts.

    Responsibilities:
    - Load the trained ML model and scaler into memory
    - Start the background listener thread that:
        * listens for new database events indicating new feature data is available
        * triggers predictions on new incoming feature data

    This ensures:
    - Model is loaded only once at startup, not on every request
    - Listener runs continuously in the background
    """

    load_model()

    # Start listener in background thread
    thread = threading.Thread(target=listen_for_updates, daemon=True)
    thread.start()

@app.get("/latest")
def get_pred_next_candle():
    """
    Retrieve the latest trading prediction generated by the background listener.

    Returns:
    - "buy"  → model predicts upward movement
    - "sell" → model predicts downward movement
    - "hold" → no strong signal / neutral prediction
    - "no prediction yet" → system has not produced any prediction yet
    """

    pred_next_candle = get_latest_prediction()

    if pred_next_candle is None:
        return {"status": "no prediction yet"}
    with prediction_lock:
        if pred_next_candle == 0:
            return {"The trading decision for the next candle is ": "hold"}
        elif pred_next_candle == 1:
            return {"The trading decision for the next candle is ": "buy"}
        elif pred_next_candle == -1:
            return {"The trading decision for the next candle is ": "sell"}
